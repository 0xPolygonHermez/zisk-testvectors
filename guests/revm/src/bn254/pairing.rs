struct EcPairingTestCase {
    name: String,
    pairs: Vec<([u8; 64], [u8; 128])>,
    expected: bool,
}

fn parse_ecpairing_test(test: &PrecompileTestCase) -> EcPairingTestCase {
    let input = &test.input;
    let num_pairs = input.len() / 192;

    let pairs = (0..num_pairs)
        .map(|i| {
            let mut g1 = [0u8; 64];
            let mut g2 = [0u8; 128];
            g1.copy_from_slice(&input[i * 192..i * 192 + 64]);
            g2.copy_from_slice(&input[i * 192 + 64..(i + 1) * 192]);
            (g1, g2)
        })
        .collect();

    let bytes = test.expected.unwrap_success();
    let expected = bytes.last().copied().unwrap_or(0) == 1;

    EcPairingTestCase { name: test.name.clone(), pairs, expected }
}

